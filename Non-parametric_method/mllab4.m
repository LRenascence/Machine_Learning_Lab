load sample_ex4.mat w1 w2 w3;
%Parzen窗
x1=[0.5;1.0;0.0];
x2=[0.31;1.51;-0.5];
x3=[-0.3;0.44;-0.1];
%当h=1的情况
h=1;
parzen([w1,w2,w3],x1,h);
parzen([w1,w2,w3],x2,h);
parzen([w1,w2,w3],x3,h);
%h=0.1的情况
h=0.1;
parzen([w1,w2,w3],x1,h);
parzen([w1,w2,w3],x2,h);
parzen([w1,w2,w3],x3,h);
%knn算法
%(a)题，在一维空间计算
knn_onedimension(w3(:,1),1);
knn_onedimension(w3(:,1),3);
knn_onedimension(w3(:,1),5);
%(b)题，在二维空间计算
knn_twodimension(w2(:,1:2),1);
knn_twodimension(w2(:,1:2),3);
knn_twodimension(w2(:,1:2),5);
%(c)题，在三位空间计算
x=[-0.41;0.82;0.88];
knn_threedimension(w1,w2,w3,4,x);
x=[0.14;0.72;4.1];
knn_threedimension(w1,w2,w3,4,x);
x=[-0.81;0.61;-0.38];
knn_threedimension(w1,w2,w3,4,x);